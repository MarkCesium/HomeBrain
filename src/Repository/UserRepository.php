    /**
     * @param int $userId
     * @return mixed
     */
    public function fingUserPlates(int $userId)
    {
        $query = $this->getEntityManager()->createQueryBuilder()
            ->select('ua')
            ->from(UserApi::class, 'ua')
            ->join('ua.user', 'u', 'ua.user=u.id')
            ->where('u.id = :userId')
            ->setParameter('userId', $userId)
            ->getQuery()
        ;

        return $query->getResult();
    }
